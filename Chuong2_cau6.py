# /   : Division (returns a float)
result = 7 / 2    # result = 3.5

# //  : Floor Division (returns the largest integer less than or equal to the result)
result = 7 // 2   # result = 3

# %   : Modulus (returns the remainder)
result = 7 % 2    # result = 1

# **  : Exponentiation (power)
result = 2 ** 3   # result = 8

# and : Logical AND (returns True if both operands are True)
result = True and False   # result = False

# or  : Logical OR (returns True if at least one operand is True)
result = True or False    # result = True

# is  : Identity operator (checks if two variables refer to the same object)
a = [1, 2]
b = a
result = a is b          # result = True
c = [1, 2]
result = a is c          # result = False